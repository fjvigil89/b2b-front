class Polls extends Component {

  static propTypes = {
    SetCurrentPosition: PropTypes.func.isRequired,
    position: PropTypes.number
  };

  static defaultProps = {
    position: 0,
  };

  componentWillMount = () => {
  };

  onPageChange(position){
    this.props.SetCurrentPosition(position+1);
  }

  onViewableItemsChanged = ({ viewableItems}) => {
    const visibleItemsCount = viewableItems.length;
    if(visibleItemsCount !== 0) {
      this.props.SetCurrentPosition(viewableItems[visibleItemsCount-1].index);
    }
  }

  render = () => {
    const { position } = this.props;
    return (
      <Container>
        <Header style={{ borderBottomWidth: 0 }}>
          <Left>
          <Button transparent onPress={Actions.drawerOpen}>
              <Icon name="menu" />
          </Button>
          </Left>
          <Body>
          <Title>Encuestas</Title>
          </Body>
          <Right />
        </Header>
        <View style={styles.container}>
            <View style={styles.stepIndicator}>
              <StepIndicator
                customStyles={stepIndicatorStyles}
                stepCount={10}
                direction='vertical'
                currentPosition={position}
                // labels={dummyData.map(item => item)}
                />
            </View>
            <FlatList
              style={{flexGrow:1}}
              data={dummyData}
              renderItem={
                (item) => 
                <View style={styles.rowItem}>
                  <Text style={styles.title}>{item.item.title}</Text>
                  <Text style={styles.body}>{item.item.body}</Text>
                </View>
              } 
              onViewableItemsChanged={this.onViewableItemsChanged}
              // viewabilityConfig={this.viewabilityConfig}
              keyExtractor={item => item.title}
            />
        </View>
      </Container>
    );
  };
}


const mapStateToProps = state => ({
  position: state.polls.position,
});

const mapDispatchToProps = {
  SetCurrentPosition
};

export default connect(mapStateToProps, mapDispatchToProps)(Polls);